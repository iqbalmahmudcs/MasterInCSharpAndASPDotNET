int number1 = 10, number2 = 11;

Console.WriteLine($"{number1} & {number2} = {number1 & number2}");
Console.WriteLine($"{number1} | {number2} = {number1 | number2}");
Console.WriteLine($"{number1} ^ {number2} = {number1 ^ number2}");
Console.WriteLine($"{number1} >> {number2} = {number1 >> number2}");
Console.WriteLine($"{number1} << {number2} = {number1 << number2}");