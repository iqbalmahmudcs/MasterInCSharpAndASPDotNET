int number1 = 10;

Console.WriteLine($"{number1}++  = {number1++}");  //post Increment
Console.WriteLine($"++{number1}  = {++number1}");  //Pre Increment
Console.WriteLine($"{number1}--  = {number1--}");  //Post decrement
Console.WriteLine($"--{number1}  = {--number1}");  //Pre decrement
Console.WriteLine($"+ {number1}  = {+number1}");  
Console.WriteLine($"- {number1}  = {-number1}");